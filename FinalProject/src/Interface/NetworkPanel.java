    private void populateNetworkTable() {
         DefaultTableModel dtm = (DefaultTableModel) networkTable.getModel();
        dtm.setRowCount(0);
            for (Network network : netDir.getNetworkDir()) {
               Object[] row = new Object[dtm.getColumnCount()];
//                Object[] row = new Object[1];
                row[0] = network;
                dtm.addRow(row);
                
            }

    }
