    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
       String network = networkItem.getSelectedItem().toString();
        String enttype = entType.getSelectedItem().toString();
        String entname = entName.getText();
        Enterprise enterprise = enterpriseDirectory.addEnterprise();
        enterprise.setEntType(enttype);
        enterprise.setNetwork(network);
        enterprise.setEntName(entname);
        JOptionPane.showMessageDialog(null, "Configured an enterprise successfully");
        entName.setText("");
        populateTable(); 
        
    }//GEN-LAST:event_btnSubmitActionPerformed

