    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) enterpriseTable.getModel();
        dtm.setRowCount(0);
        for (Enterprise entAd : enterpriseDirectory.getEnterpriseDirectory()) {
           Object[] row = new Object[dtm.getColumnCount()];
           row[0] = entAd;
           row[1] = entAd.getNetwork();
           row[2] = entAd.getEntType();
           dtm.addRow(row);
        }
    }

