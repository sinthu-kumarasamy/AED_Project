    private void populateBarChart(HashMap<String,Integer>dataMap){
        DefaultCategoryDataset dcd = new DefaultCategoryDataset();
        Set keys = dataMap.keySet();
        Iterator entIterator = keys.iterator();
        while(entIterator.hasNext()){
            String entname = String.valueOf(entIterator.next());
            int no_of_users = dataMap.get(entname);
            dcd.setValue(no_of_users,"No. of Beneficiaries",entname);
        }
        JFreeChart chart = ChartFactory.createBarChart3D("Enrolled Beneficiary record","Enterprise Name","No. of Beneficiaries",dcd, PlotOrientation.VERTICAL, true, true, false);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.BLACK);
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setVisible(true);
        Panel1.removeAll();
        Panel1.add(chartPanel,BorderLayout.CENTER);
        Panel1.validate();
        Panel1.setVisible(true);
    }
    
    private void populatePieChart(HashMap<String,Integer>dataBenMap){
         DefaultPieDataset dcd = new DefaultPieDataset();
        Set keys = dataBenMap.keySet();
        Iterator entIterator = keys.iterator();
        while(entIterator.hasNext()){
            String entname = String.valueOf(entIterator.next());
            int no_of_users = dataBenMap.get(entname);
            dcd.setValue(entname,no_of_users);
        }
        JFreeChart chart = ChartFactory.createPieChart3D("Beneficiary Record",dcd, true, true, true);
        PiePlot3D plot =(PiePlot3D) chart.getPlot();
        ChartPanel chartPanel1 = new ChartPanel(chart);
        chartPanel1.setVisible(true);
        Panel1.setLayout(new GridLayout());
        Panel1.add(chartPanel1);
        Panel1.validate();
        Panel1.setVisible(true);
        
    }

