    private void createChartData(){
        HashMap<String,Integer> dataMap = new HashMap<String,Integer>();
        HashMap<String,Integer> dataBenMap = new HashMap<String,Integer>();
        int no_of_ben = 0;
        int maxCount = 0;
        for(Network net:system.getNetworkList()){
            for(WelfareEnterprise ent:net.getEnterpriseDirectory().getWelfareEnterpriseList()){
                for(WelfareOrganization org:ent.getWelfareOrganizationDirectory().getWelfareOrganizationList()){
                    if(!ent.getName().equals("Admin") && org.getName().equals("Beneficiary Organization")){
                        dataMap.put(String.valueOf(ent),org.getBeneficiaryDirectory().getBeneficiaryDirectory().size());
                        for(Beneficiary ben:org.getBeneficiaryDirectory().getBeneficiaryDirectory()){
                            if(ben.getStatus()!=null){
                                no_of_ben++;
                            }
                        }
                        maxCount = no_of_ben;
                        no_of_ben = 0;
                        dataBenMap.put(String.valueOf(ent),maxCount);
                    }
                }   
            }
        }
        populateBarChart(dataMap);
        populatePieChart(dataBenMap);
    }
